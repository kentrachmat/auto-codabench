def write_solution(slug: str, files: dict[str, str], *, subdir: str = "solution_baseline", root_dir: str | None = None) -> dict[str, Any]:
    """Write a baseline solution as a folder of text files under solutions/<subdir>/.

    `files` is a mapping {relative_path_within_solution: file_body}. We do not
    handle binary data here — for binaries the caller should drop files in
    place and call attach_data with from_path=, which copies bytes.
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")
    if not files:
        raise ValueError("write_solution requires at least one file")

    sol_dir = bundle / BUNDLE_LAYOUT["solutions"] / subdir
    sol_dir.mkdir(parents=True, exist_ok=True)

    written: list[str] = []
    for rel_str, body in files.items():
        rel = Path(rel_str)
        if rel.is_absolute() or ".." in rel.parts:
            raise ValueError(f"solution file path must be relative inside the bundle: {rel_str!r}")
        path = sol_dir / rel
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(body, encoding="utf-8")
        written.append(str(Path(BUNDLE_LAYOUT["solutions"]) / subdir / rel))
    return {"bundle_dir": str(bundle), "written": written}


def attach_data(
    slug: str,
    *,
    target: str,
    files: dict[str, str] | None = None,
    from_path: str | None = None,
    root_dir: str | None = None,
) -> dict[str, Any]:
    """Place files into one of the bundle's data directories.

    Args:
        target: one of "reference_data", "input_data", "starting_kit", "public_data"
        files:  mapping {relative_path: text content} written as UTF-8 text
        from_path: if set, copy this file or directory tree into the target dir.
                   `files` and `from_path` are mutually exclusive.
    """
    if target not in BUNDLE_LAYOUT or target in {"pages", "scoring_program", "ingestion_program", "solutions"}:
        raise ValueError(
            f"attach_data target must be one of "
            f"reference_data|input_data|starting_kit|public_data, got {target!r}"
        )
    if (files is None) == (from_path is None):
        raise ValueError("provide exactly one of `files` or `from_path`")

    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")

    target_dir = bundle / BUNDLE_LAYOUT[target]
    target_dir.mkdir(parents=True, exist_ok=True)
    written: list[str] = []

    if files is not None:
        for rel_str, body in files.items():
            rel = Path(rel_str)
            if rel.is_absolute() or ".." in rel.parts:
                raise ValueError(f"file path must be relative inside the bundle: {rel_str!r}")
            path = target_dir / rel
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(body, encoding="utf-8")
            written.append(str(Path(BUNDLE_LAYOUT[target]) / rel))
    else:
        src = Path(from_path).expanduser().resolve()
        if not src.exists():
            raise FileNotFoundError(f"from_path does not exist: {src}")
        if src.is_dir():
            for p in src.rglob("*"):
                if p.is_file():
                    rel = p.relative_to(src)
                    dst = target_dir / rel
                    dst.parent.mkdir(parents=True, exist_ok=True)
                    shutil.copy2(p, dst)
                    written.append(str(Path(BUNDLE_LAYOUT[target]) / rel))
        else:
            dst = target_dir / src.name
            shutil.copy2(src, dst)
            written.append(str(Path(BUNDLE_LAYOUT[target]) / src.name))

    return {"bundle_dir": str(bundle), "target": target, "written": written}


