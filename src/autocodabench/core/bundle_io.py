@dataclass
class ValidationIssue:
    severity: str  # "error" | "warning"
    where: str     # human-readable locator
    message: str

    def to_dict(self) -> dict[str, str]:
        return {"severity": self.severity, "where": self.where, "message": self.message}


def _load_yaml(path: Path) -> Any:
    return yaml.safe_load(path.read_text(encoding="utf-8"))


def validate_bundle(slug: str, root_dir: str | None = None) -> dict[str, Any]:
    """Local lint pass over a bundle. Returns issues; does not modify files.

    Checks (everything is best-effort but rules out the most common errors
    that cause uploads to be rejected):

      1. competition.yaml parses and has required top-level keys.
      2. Every file referenced from competition.yaml exists.
      3. Every leaderboard column.key appears at least once as a key written by
         the scoring program (we statically scan score.py text for
         `json.dump(...)` or `scores.json` keys; if we can't be sure, we warn).
      4. Each scoring/ingestion program has a metadata.yaml with a `command:`.
      5. Phases are sorted, do not overlap, and reference declared tasks.
      6. Tasks referenced from phases/solutions exist by index.
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    issues: list[ValidationIssue] = []

    yaml_path = bundle / "competition.yaml"
    if not yaml_path.exists():
        return {
            "bundle_dir": str(bundle),
            "ok": False,
            "issues": [ValidationIssue("error", "competition.yaml", "missing").to_dict()],
        }

    try:
        comp = _load_yaml(yaml_path)
    except yaml.YAMLError as e:
        return {
            "bundle_dir": str(bundle),
            "ok": False,
            "issues": [ValidationIssue("error", "competition.yaml", f"YAML parse error: {e}").to_dict()],
        }
    if not isinstance(comp, dict):
        return {
            "bundle_dir": str(bundle),
            "ok": False,
            "issues": [ValidationIssue("error", "competition.yaml", "top level must be a mapping").to_dict()],
        }

    # 1. required keys
    for k in _COMPETITION_KEYS_REQUIRED:
        if k not in comp:
            issues.append(ValidationIssue("error", "competition.yaml", f"missing required key '{k}'"))

    # helper to resolve path references
    def _resolve(rel: str) -> Path:
        return (bundle / rel).resolve()

    # 2. file references
    for key in ("image", "terms"):
        ref = comp.get(key)
        if isinstance(ref, str) and not _resolve(ref).exists():
            issues.append(ValidationIssue("error", f"competition.yaml:{key}", f"referenced file does not exist: {ref}"))

    pages = comp.get("pages") or []
    for i, page in enumerate(pages):
        if not isinstance(page, dict):
            issues.append(ValidationIssue("error", f"pages[{i}]", "entry must be a mapping"))
            continue
        for k in ("title", "file"):
            if k not in page:
                issues.append(ValidationIssue("error", f"pages[{i}]", f"missing '{k}'"))
        ref = page.get("file")
        if isinstance(ref, str) and not _resolve(ref).exists():
            issues.append(ValidationIssue("error", f"pages[{i}].file", f"page file does not exist: {ref}"))

    # 6. task index lookup
    tasks = comp.get("tasks") or []
    task_by_index: dict[int, dict[str, Any]] = {}
    for i, t in enumerate(tasks):
        if not isinstance(t, dict):
            issues.append(ValidationIssue("error", f"tasks[{i}]", "entry must be a mapping"))
            continue
        idx = t.get("index")
        if not isinstance(idx, int):
            issues.append(ValidationIssue("error", f"tasks[{i}]", "missing integer 'index'"))
            continue
        if idx in task_by_index:
            issues.append(ValidationIssue("error", f"tasks[{i}]", f"duplicate task index {idx}"))
        task_by_index[idx] = t

        # task file references
        for key in ("input_data", "reference_data", "scoring_program", "ingestion_program"):
            ref = t.get(key)
            if isinstance(ref, str) and not _resolve(ref).exists():
                issues.append(ValidationIssue("error", f"tasks[{i}].{key}", f"referenced path does not exist: {ref}"))

        # If a program is a directory, it must carry a runnable metadata file.
        # Codabench accepts BOTH `metadata.yaml` and the legacy extensionless
        # `metadata` (production bundles use either — verified against the
        # STYLE-TRANS-FAIR reference bundle, Codabench competition #601).
        def _program_metadata(path: Path) -> Path | None:
            for candidate in ("metadata.yaml", "metadata"):
                if (path / candidate).is_file():
                    return path / candidate
            return None

        sp = t.get("scoring_program")
        if isinstance(sp, str):
            sp_path = _resolve(sp)
            if sp_path.is_dir():
                meta = _program_metadata(sp_path)
                if meta is None:
                    issues.append(ValidationIssue("error", f"tasks[{i}].scoring_program", "missing metadata.yaml (or legacy 'metadata')"))
                else:
                    try:
                        meta_data = _load_yaml(meta)
                        if not isinstance(meta_data, dict) or not meta_data.get("command"):
                            issues.append(ValidationIssue("error", f"tasks[{i}].scoring_program/{meta.name}", "missing 'command'"))
                    except yaml.YAMLError as e:
                        issues.append(ValidationIssue("error", f"tasks[{i}].scoring_program/{meta.name}", f"YAML parse error: {e}"))
        ip = t.get("ingestion_program")
        if isinstance(ip, str):
            ip_path = _resolve(ip)
            if ip_path.is_dir():
                if _program_metadata(ip_path) is None:
                    issues.append(ValidationIssue("error", f"tasks[{i}].ingestion_program", "missing metadata.yaml (or legacy 'metadata')"))

    # 5. phases
    phases = comp.get("phases") or []
    last_end = None
    for i, p in enumerate(phases):
        if not isinstance(p, dict):
            issues.append(ValidationIssue("error", f"phases[{i}]", "entry must be a mapping"))
            continue
        for k in ("name", "start"):
            if k not in p:
                issues.append(ValidationIssue("error", f"phases[{i}]", f"missing required '{k}'"))
        for t_idx in p.get("tasks") or []:
            if t_idx not in task_by_index:
                issues.append(ValidationIssue("error", f"phases[{i}].tasks", f"references unknown task index {t_idx}"))
        # date ordering: only catch the trivially broken case
        s, e = p.get("start"), p.get("end")
        if isinstance(s, str) and isinstance(e, str) and s > e:
            issues.append(ValidationIssue("error", f"phases[{i}]", f"start ({s}) is after end ({e})"))
        if last_end is not None and isinstance(s, str) and isinstance(last_end, str) and s < last_end:
            issues.append(ValidationIssue("warning", f"phases[{i}]", f"start ({s}) precedes previous phase end ({last_end})"))
        if isinstance(e, str):
            last_end = e

    # solutions
    for i, sol in enumerate(comp.get("solutions") or []):
        if not isinstance(sol, dict):
            issues.append(ValidationIssue("error", f"solutions[{i}]", "entry must be a mapping"))
            continue
        ref = sol.get("path")
        if isinstance(ref, str) and not _resolve(ref).exists():
            issues.append(ValidationIssue("error", f"solutions[{i}].path", f"path does not exist: {ref}"))
        for t_idx in sol.get("tasks") or []:
            if t_idx not in task_by_index:
                issues.append(ValidationIssue("error", f"solutions[{i}].tasks", f"references unknown task index {t_idx}"))

    # 3. leaderboards ↔ scoring program scores.json keys
    leaderboards = comp.get("leaderboards") or []
    expected_keys: set[str] = set()
    for i, lb in enumerate(leaderboards):
        if not isinstance(lb, dict):
            issues.append(ValidationIssue("error", f"leaderboards[{i}]", "entry must be a mapping"))
            continue
        for k in ("title", "key", "columns"):
            if k not in lb:
                issues.append(ValidationIssue("error", f"leaderboards[{i}]", f"missing required '{k}'"))
        for j, col in enumerate(lb.get("columns") or []):
            if not isinstance(col, dict):
                issues.append(ValidationIssue("error", f"leaderboards[{i}].columns[{j}]", "must be a mapping"))
                continue
            for k in ("title", "key", "index"):
                if k not in col:
                    issues.append(ValidationIssue("error", f"leaderboards[{i}].columns[{j}]", f"missing '{k}'"))
            # 'computation' columns are derived — they don't need to appear in scores.json
            if "computation" in col:
                continue
            if isinstance(col.get("key"), str):
                expected_keys.add(col["key"])

    # Static scan: do score.py files literally write each expected key?
    score_files: list[Path] = []
    for sp_dir in [bundle / BUNDLE_LAYOUT["scoring_program"]]:
        if sp_dir.is_dir():
            score_files.extend(p for p in sp_dir.rglob("*.py"))
    for t in tasks:
        sp = t.get("scoring_program") if isinstance(t, dict) else None
        if isinstance(sp, str):
            sp_path = _resolve(sp)
            if sp_path.is_dir():
                score_files.extend(p for p in sp_path.rglob("*.py"))
    score_text = "\n".join(p.read_text(encoding="utf-8", errors="replace") for p in score_files)
    for key in sorted(expected_keys):
        if score_text and (f"'{key}'" not in score_text and f'"{key}"' not in score_text):
            issues.append(ValidationIssue(
                "warning",
                "leaderboards ↔ scoring",
                f"leaderboard key '{key}' not found as a literal in any score.py — make sure scores.json writes it.",
            ))

    ok = not any(i.severity == "error" for i in issues)
    return {
        "bundle_dir": str(bundle),
        "ok": ok,
        "issues": [i.to_dict() for i in issues],
        "leaderboard_keys_expected": sorted(expected_keys),
    }


