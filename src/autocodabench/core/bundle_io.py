def _write_program(
    bundle: Path,
    program_dir_key: str,
    script_filename: str,
    script_body: str,
    metadata_command: str,
) -> list[str]:
    program_dir = bundle / BUNDLE_LAYOUT[program_dir_key]
    program_dir.mkdir(parents=True, exist_ok=True)

    script_path = program_dir / script_filename
    script_path.write_text(script_body, encoding="utf-8")
    os.chmod(script_path, 0o755)

    meta_path = program_dir / "metadata.yaml"
    meta_path.write_text(yaml.safe_dump({"command": metadata_command}, sort_keys=False), encoding="utf-8")

    rel_dir = BUNDLE_LAYOUT[program_dir_key]
    return [f"{rel_dir}/{script_filename}", f"{rel_dir}/metadata.yaml"]


def write_scoring_program(
    slug: str,
    script: str,
    *,
    script_filename: str = "score.py",
    command: str | None = None,
    root_dir: str | None = None,
) -> dict[str, Any]:
    """Write scoring_program/{script_filename, metadata.yaml}.

    The default command matches the path layout Codabench gives the scoring
    program at runtime (see Competition-Bundle-Structure.md):
        /app/program/<script>  /app/input  /app/output
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")

    if command is None:
        command = f"python3 /app/program/{script_filename} /app/input /app/output"

    written = _write_program(bundle, "scoring_program", script_filename, script, command)
    return {"bundle_dir": str(bundle), "written": written, "command": command}


def write_ingestion_program(
    slug: str,
    script: str,
    *,
    script_filename: str = "ingestion.py",
    command: str | None = None,
    root_dir: str | None = None,
) -> dict[str, Any]:
    """Write ingestion_program/{script_filename, metadata.yaml}.

    Default command mirrors the docs' Submission-Docker-Container-Layout:
        python3 /app/program/<script> /app/input_data /app/output /app/program /app/ingested_program
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")

    if command is None:
        command = (
            f"python3 /app/program/{script_filename} "
            "/app/input_data /app/output /app/program /app/ingested_program"
        )

    written = _write_program(bundle, "ingestion_program", script_filename, script, command)
    return {"bundle_dir": str(bundle), "written": written, "command": command}


