def zip_bundle(slug: str, root_dir: str | None = None, output: str | None = None) -> dict[str, Any]:
    """Produce <slug>.zip with competition.yaml AT THE ROOT of the zip.

    By far the most common bundle upload error is zipping the *containing
    folder* rather than its contents. We always zip contents-only so that
    `competition.yaml` is at the zip's root.
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")
    yaml_at_root = bundle / "competition.yaml"
    if not yaml_at_root.exists():
        raise FileNotFoundError(f"competition.yaml missing at {yaml_at_root}; nothing to zip")

    out_path = Path(output).expanduser().resolve() if output else (bundle.parent / f"{slug}.zip")
    out_path.parent.mkdir(parents=True, exist_ok=True)
    if out_path.exists():
        out_path.unlink()

    with zipfile.ZipFile(out_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for p in bundle.rglob("*"):
            if p.is_file():
                zf.write(p, p.relative_to(bundle))
    return {
        "bundle_dir": str(bundle),
        "zip_path": str(out_path),
        "size_bytes": out_path.stat().st_size,
    }


