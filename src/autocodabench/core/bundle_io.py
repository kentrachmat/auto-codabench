"""Data layer for bundle authoring. No MCP, no FastMCP — just file I/O on dicts.

Every function in this module:
  - takes a bundle root Path and primitive data
  - writes (or reads) deterministic files inside that bundle
  - returns plain dicts with a 'written' list of relative paths

This separation lets us run the data layer in isolation (a CLI smoke test in
__main__) without dragging MCP into the loop.
"""
from __future__ import annotations

import json
import os
import shutil
import textwrap
import zipfile
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import yaml  # PyYAML — already a dep via fastmcp

from .config import BUNDLE_LAYOUT, resolve_bundle_dir


# ---------------------------------------------------------------------------
# Init / skeleton
# ---------------------------------------------------------------------------

def init_bundle(slug: str, root_dir: str | None = None, overwrite: bool = False) -> dict[str, Any]:
    """Create the empty directory skeleton for a Codabench bundle.

    Returns the resolved bundle path and the directories that were created.
    Does NOT write competition.yaml — call write_competition_yaml for that.
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if bundle.exists():
        if not overwrite:
            return {
                "bundle_dir": str(bundle),
                "created": False,
                "reason": "exists; pass overwrite=True to replace",
            }
        shutil.rmtree(bundle)

    bundle.mkdir(parents=True, exist_ok=False)
    created_dirs: list[str] = []
    for d in BUNDLE_LAYOUT.values():
        (bundle / d).mkdir(parents=True, exist_ok=True)
        created_dirs.append(d)

    # A README so the agent and the human can both see what this folder is.
    readme = bundle / "README.md"
    readme.write_text(
        textwrap.dedent(
            f"""\
            # Bundle: {slug}

            Generated by autocodabench. To inspect, run the bundle's metadata.yaml
            check or invoke validate_bundle / zip_bundle from the MCP server.

            Layout:

            - `competition.yaml`           — written by write_competition_yaml
            - `pages/`                     — markdown pages referenced from competition.yaml
            - `scoring_program/`           — score.py + metadata.yaml
            - `ingestion_program/`         — (optional) ingestion.py + metadata.yaml
            - `reference_data/`            — ground truth used by scoring program
            - `input_data/`                — (optional) inputs given to a code submission
            - `solutions/`                 — example submission used as baseline + smoke test
            - `starting_kit/`              — files for participants to download
            - `public_data/`               — (optional) publicly downloadable data
            """
        ),
        encoding="utf-8",
    )
    return {
        "bundle_dir": str(bundle),
        "created": True,
        "directories": created_dirs,
        "readme": "README.md",
    }


