# Allow-listed top-level keys we will accept from callers, matching
# Yaml-Structure.md. Unknown keys are rejected to catch typos early.
_COMPETITION_KEYS_REQUIRED = {"version", "title", "image", "terms", "pages", "phases", "tasks", "leaderboards"}
_COMPETITION_KEYS_OPTIONAL = {
    "description", "registration_auto_approve", "docker_image",
    "make_programs_available", "make_input_data_available",
    "enable_detailed_results", "show_detailed_results_in_submission_panel",
    "show_detailed_results_in_leaderboard", "contact_email", "reward",
    "auto_run_submissions", "can_participants_make_submissions_public",
    "forum_enabled", "queue", "solutions", "fact_sheet",
}
_COMPETITION_KEYS_ALL = _COMPETITION_KEYS_REQUIRED | _COMPETITION_KEYS_OPTIONAL


def write_competition_yaml(slug: str, payload: dict[str, Any], root_dir: str | None = None) -> dict[str, Any]:
    """Write competition.yaml at the bundle root.

    payload is the full YAML body as a dict. We validate keys against the
    Codabench spec and reject unknowns rather than silently writing them —
    the model should fail loudly on misnamed keys.
    """
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")

    if not isinstance(payload, dict):
        raise TypeError("payload must be a dict")

    payload.setdefault("version", 2)

    bad = set(payload.keys()) - _COMPETITION_KEYS_ALL
    if bad:
        raise ValueError(f"unknown competition.yaml keys: {sorted(bad)}")

    missing = _COMPETITION_KEYS_REQUIRED - set(payload.keys())
    if missing:
        raise ValueError(f"missing required competition.yaml keys: {sorted(missing)}")

    out_path = bundle / "competition.yaml"
    out_path.write_text(yaml.safe_dump(payload, sort_keys=False, allow_unicode=True), encoding="utf-8")
    return {"bundle_dir": str(bundle), "written": ["competition.yaml"]}


