_ALLOWED_PAGE_EXTENSIONS = {".md", ".html"}


def write_page(slug: str, filename: str, body: str, root_dir: str | None = None) -> dict[str, Any]:
    """Write a single page markdown/html file under pages/."""
    bundle = resolve_bundle_dir(slug, root_dir)
    if not bundle.exists():
        raise FileNotFoundError(f"bundle not initialised: {bundle}")

    rel = Path(filename)
    if rel.is_absolute() or ".." in rel.parts:
        raise ValueError(f"page filename must be relative inside the bundle: {filename!r}")
    if rel.suffix.lower() not in _ALLOWED_PAGE_EXTENSIONS:
        raise ValueError(f"page filename must end in .md or .html, got: {filename!r}")

    # Default: under pages/ unless the caller already prefixed it.
    if rel.parts[0] != BUNDLE_LAYOUT["pages"]:
        rel = Path(BUNDLE_LAYOUT["pages"]) / rel

    target = bundle / rel
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(body, encoding="utf-8")
    return {"bundle_dir": str(bundle), "written": [str(rel)]}


